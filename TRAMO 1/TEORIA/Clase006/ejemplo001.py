def cuadrado(x):   
    r = x*x    
    return r


def main(): # PROGRAMA PRINCIPAL

    resultado = cuadrado(2)
    print(f"cuadrado(2) -> {resultado}")

    resultado = cuadrado(3)
    print(f"cuadrado(3) -> {resultado}")

    x = 8
    print(f"cuadrado(x) cuando x vale {x} -> {cuadrado(x)}")

    pepe = 88
    print(f"cuadrado(x) cuando x vale {pepe} -> {cuadrado(pepe)}")

    print(f"cuadrado(cuadrado(x)) cuando x vale {pepe} -> {cuadrado(cuadrado(pepe))}")



main()
     


